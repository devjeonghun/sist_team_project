package com.aven.model;

public class RoomBean {
	private int h_no;
	private int r_no;
	private String r_concep;
	private int r_price;
	private int r_count;
	private String r_option;
	private String r_file;
	private int rflag;
	
	public int getRflag() {
		return rflag;
	}
	public void setRflag(int rflag) {
		this.rflag = rflag;
	}
	public int getH_no() {
		return h_no;
	}
	public void setH_no(int h_no) {
		this.h_no = h_no;
	}
	public int getR_no() {
		return r_no;
	}
	public void setR_no(int r_no) {
		this.r_no = r_no;
	}
	public String getR_concep() {
		return r_concep;
	}
	public void setR_concep(String r_concep) {
		this.r_concep = r_concep;
	}
	public int getR_price() {
		return r_price;
	}
	public void setR_price(int r_price) {
		this.r_price = r_price;
	}
	public int getR_count() {
		return r_count;
	}
	public void setR_count(int r_count) {
		this.r_count = r_count;
	}

	public String getR_option() {
		return r_option;
	}
	public void setR_option(String r_option) {
		this.r_option = r_option;
	}
	public String getR_file() {
		return r_file;
	}
	public void setR_file(String r_file) {
		this.r_file = r_file;
	}
	@Override
	public String toString() {
		return "RoomBean [h_no=" + h_no + ", r_no=" + r_no + ", r_concep=" + r_concep + ", r_price=" + r_price
				+ ", r_count=" + r_count + ", r_option=" + r_option + ", r_file=" + r_file + ", rflag=" + rflag + "]";
	}
	

}
